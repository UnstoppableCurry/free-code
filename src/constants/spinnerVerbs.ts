import { getInitialSettings } from '../utils/settings/settings.js'
import { resolveLocaleFromEnv } from '../i18n/translator.js'

export function getSpinnerVerbs(): string[] {
  const settings = getInitialSettings()
  const config = settings.spinnerVerbs
  const baseVerbs = getLocalizedSpinnerVerbs()
  if (!config) {
    return baseVerbs
  }
  if (config.mode === 'replace') {
    return config.verbs.length > 0 ? config.verbs : baseVerbs
  }
  return [...baseVerbs, ...config.verbs]
}

/**
 * Locale-aware spinner verb list. Returns SPINNER_VERBS_ZH when running with
 * a Chinese locale (FREE_CODE_LANG / LANG / LC_*); otherwise the English list.
 */
export function getLocalizedSpinnerVerbs(): string[] {
  return resolveLocaleFromEnv(process.env) === 'zh-CN'
    ? SPINNER_VERBS_ZH
    : SPINNER_VERBS
}

